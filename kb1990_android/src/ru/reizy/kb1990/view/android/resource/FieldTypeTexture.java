	private void put(FieldType f, int p) {
		BitmapDrawable b = ((BitmapDrawable) getResources().getDrawable(p));
		put(f, b);
	}

	private void put(FieldType f, BitmapDrawable p) {
		bg.put(f, p);
	}

	private BitmapDrawable getDefault() {
		BitmapDrawable p = bg.get(POOL_O);
		return p;
	}

	public BitmapDrawable getTexture(FieldType f) {
		BitmapDrawable p = null;
		if (f != null) {
			p = bg.get(f);
		}
		p = (p != null) ? p : getDefault();
		return p;
	}

	public Drawable getActive(Content с) {
		Drawable p = null;
		if (с != null) {
			p = active.get(с);
		}
		p = (p != null) ? p : getDefault();
		return p;
	}

	public BitmapDrawable getUnit(UnitTypes unit) {
		BitmapDrawable p = null;
		if (unit != null) {
			int img_id = units.get_l(unit);
			p = ((BitmapDrawable) getResources().getDrawable(img_id));
		}
		p = (p != null) ? p : getDefault();
		return p;
	}
